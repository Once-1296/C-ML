#ifndef SIGMOID_H
#define SIGMOID_H

/**
 * @brief Applies the sigmoid activation function.
 *
 * The sigmoid activation function is defined as:
 * - f(x) = 1 / (1 + exp(-x))
 *
 * @param x The input value.
 * @return The result of the sigmoid activation function.
 */
float sigmoid(float x);

#endif // SIGMOID_H
