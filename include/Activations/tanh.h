#ifndef TANH_H
#define TANH_H

/**
 * @brief Applies the hyperbolic tangent (tanh) activation function.
 *
 * The tanh activation function is defined as:
 * - f(x) = (e^x - e^(-x)) / (e^x + e^(-x))
 *
 * @param x The input value.
 * @return The result of the tanh activation function.
 */
float tanH(float x);

#endif // TANH_H
